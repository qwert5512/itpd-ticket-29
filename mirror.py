def max_bottles(a, b, c):
    """
    Функція визначає максимальну кількість пляшок,
    яку можна купити за наявні кошти та обмежену кількість товару.
    """
    can_buy = b // a
    return min(can_buy, c)


# Основна програма
a = int(input("Введіть ціну однієї пляшки (a): "))
b = int(input("Введіть кількість грошей (b): "))
c = int(input("Введіть кількість пляшок в автоматі (c): "))

result = max_bottles(a, b, c)
print("Максимальна кількість пляшок:", result)
